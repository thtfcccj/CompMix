/******************************************************************
                  系统Us级Tick模块
此模块使SysTick为1ms为单位值, 但提供以1us为单位的查询功能
********************************************************************/
#ifndef _SYS_TICK_US_H
#define _SYS_TICK_US_H

/******************************************************************
                  相关结构
******************************************************************/

//定义系统时基变量,Us为单位
#define   TickType_t  unsigned long


/******************************************************************
                    相关函数接口
******************************************************************/
//------------------------时基初始化函数------------------------
//初始化为产生1ms时基
void SysTickUs_Init(void);

//---------------------得到当前自由定时器Us值--------------------
TickType_t SysTickUs_Get(void);

//---------------------时基定时器0查询方式------------------------
//放快速进程中，或长延时进程中防止错过时间
//在此函数中,硬件时基到时
void SysTickUs_FastTask(void);

/******************************************************************
                        回调函数
******************************************************************/

//-----------------------------定时器通报---------------------------
//1Tick(1ms)到将调用此函数
void SysTick_cbHandlerNotify(void); //外部实现

#endif
